public class caseswitch{
    public static void main(String[] args){
        String color = "blue";
        switch(color){
            case "blue":
            System.out.println("My favorit color is Blue");
            break;
            case "red":
            System.out.println("My favorit color is red");
            break;
            case "green":
            System.out.println("My favorit color is Green");
            break;
            default:
            System.out.println("My favorit color is Black");
        }
    }
}